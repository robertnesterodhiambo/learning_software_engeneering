/**
 * Enum representing the different extras that can be added to a coffee.
 */
public enum Extra {
    /**
     * Chocolate powder extra.
     */
    CHOCOLATE_POWDER,

    /**
     * Cinnamon extra.
     */
    CINNAMON,

    /**
     * Vanilla syrup extra.
     */
    VANILLA_SYRUP,

    /**
     * Whipped cream extra.
     */
    WHIPPED_CREAM,

    /**
     * Vanilla ice cream extra.
     */
    VANILLA_ICE_CREAM,

    /**
     * Chocolate syrup extra.
     */
    CHOCOLATE_SYRUP,

    /**
     * Caramel syrup extra.
     */
    CARAMEL_SYRUP,

    /**
     * Represents the option to skip adding any extras.
     */
    SKIP;
}


