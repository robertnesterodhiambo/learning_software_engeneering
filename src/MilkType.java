/**
 * Enum representing the different types of milk that can be added to a coffee.
 */
public enum MilkType {
    /**
     * Full cream milk type.
     */
    FULL_CREAM,

    /**
     * Skim milk type.
     */
    SKIM,

    /**
     * Soy milk type.
     */
    SOY,

    /**
     * Almond milk type.
     */
    ALMOND,

    /**
     * Oat milk type.
     */
    OAT,

    /**
     * Represents the option of having no milk.
     */
    NO_MILK;
}


